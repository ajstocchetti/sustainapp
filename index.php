		<p>Corporate social responsibility (CSR) is the impact a business has on its surroundings, including both environmental factors and human relations. Factors such as waste and pollution, testing on animals, fair pay and livable wages, giving back through donations and volunteering all attribute to a company’s CSR. Social responsibility is the impact that people make on their surroundings. The CSR of the companies you support (through promotion, discussion, and purchasing their goods and services) has a direct impact on an individual's social responsibility.</p>
